package javaPracticeDome.cd.practice.dome;

/**
 * 变量的定义和使用
 * */
public class VariableDome {
    /*
    * 变量的概述
    * 从字面上看，就是在程序执行过程中，可以在某个范围可以改变的量，本质上就是，内存的一小块区域
    * 比如：商品的价格、现在是什么时间等等，这些都是可变的，他们就可以称为变量。
    * 变量的格式
    * 数据类型+变量名=初始化值
    * */

    public static void main(String[] args) {
        // 定义变量number作为学生的个数
        int number = 12;
        // 使用变量
        System.out.println(number);
    }
}
