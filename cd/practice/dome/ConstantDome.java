/**
 * 这是一个解释常量类
 * */
public class ConstantDome {
    /*
    * 常量 （从开始执行到结束不会改变的值）
    *   "123456"   字符串常量
        1、2       整数常量
        1.2456     小数常量
        '1'        字符常量
        true false 布尔常量
        null       空常量
    * */
    public static void main(String[] args) {
        System.out.println("字符串常量"+"abc");
        System.out.println("字符常量"+'a');
        System.out.println("整数常量"+1);
        System.out.println("小数常量"+1.256);
        System.out.println("布尔常量"+true);
        System.out.println("空常量"+null);
    };
}
